public class ManyVariables {

    public static void main(String[] args) {
        int a  = 18;
        short b = 250;
        double c = 299792458.3856453;
        char d = 'B';
        float e = -3256.65F;
        boolean f = true;
        char g = 'm';
        float h  = 2.7182F;
    
    
        System.out.print( a + "\n" + b );
        System.out.printf("\n%.7f\n", c);
        System.out.println(d + "\n" + e + "\n" + f + "\n" + g + "\n" + h);
    }

    }